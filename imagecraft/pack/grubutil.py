"""GRUB utils.

Phase-B (unprivileged-container friendly) GRUB installation.

The previous flow used ``losetup`` + a real chroot with a ``devtmpfs``
mount and ran ``grub-install`` against the loop device. Both ``losetup``
and ``mount -t devtmpfs`` require capabilities that aren't available in
an unprivileged user namespace (init_user_ns CAP_SYS_ADMIN), so neither
works inside an unprivileged LXD container.

The new flow:

  * Runs *inside* the rootfs prime directory (the tree produced by
    ``mmdebstrap`` + apt). That tree IS the chroot — no image mounts,
    no loop devices.
  * Replaces the ``devtmpfs`` mount with a ``--bind`` of the host's
    ``/dev`` (LXD pre-populates a usable ``/dev``).
  * Produces the bootloader bytes (``boot.img``, ``core.img``, signed
    shim/grub EFI binaries, an ESP grub.cfg stub) and stages them so
    that:
       - the rootfs prime dir already contains ``/boot/grub/grub.cfg``
         when ``mke2fs -d`` reads it in;
       - the system-boot (ESP) prime dir already contains
         ``EFI/BOOT/BOOTX64.EFI`` and friends when ``mkfs.vfat`` +
         ``mcopy`` reads it in;
       - ``boot.img`` is ``dd``'d into MBR bytes 0-440 of the final
         disk image *after* the image is finalised;
       - ``core.img`` is ``dd``'d into the BIOS-boot partition (ef02)
         on GPT, or into the post-MBR gap on plain MBR.

Scope: amd64 only (covers both x86_64-efi UEFI and i386-pc BIOS).
arm64 / armhf / riscv64 emit a TODO progress message and return
without doing anything — see :func:`setup_grub` for the entry point.
"""

